function [dydt] = H_x (x, y, u) % y: Lagrange multipliers

    global Q

    dydt = zeros(13,1);
    
    dydt(1:3)   = zeros(3,1);
    dydt(4:6)   = -y(1:3);
    dydt(7:10)  = -2*sum(u)/Q.m * [x(9) -x(8) x(7); x(10) -x(7) -x(8); x(7) x(10) -x(9); x(8) x(9) x(10)]*[y(4); y(5); y(6)] ...
                  -1/2 * [y(8) y(9) y(10); -y(7) y(10) -y(9); -y(10) -y(7) y(8); y(9) -y(8) -y(7)]*[x(11); x(12); x(13)];
    dydt(11:13) = -1/2 * [-x(8) x(7) x(10) -x(9); -x(9) -x(10) x(7) x(8); -x(10) x(9) -x(8) x(7)]*[y(7); y(8); y(9); y(10)] ...
                  +[0 x(13) x(12); x(13) 0 x(11); x(12) x(11) 0]*diag([(Q.I(3,3)-Q.I(2,2)) (Q.I(1,1)-Q.I(3,3)) (Q.I(2,2)-Q.I(1,1))])*Q.I^(-1)*y(11:13);

end